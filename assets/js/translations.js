// Current language (default: Spanish)
// Hacemos que currentLanguage sea accesible globalmente
window.currentLanguage = localStorage.getItem('language') || 'es';

// Function to change the language
function changeLanguage(lang) {
    console.log('changeLanguage llamada con idioma:', lang);
    window.currentLanguage = lang;
    localStorage.setItem('language', lang);
    console.log('Idioma guardado en localStorage:', localStorage.getItem('language'));
    console.log('window.currentLanguage actualizado a:', window.currentLanguage);
    
    // Traducir la página
    console.log('Llamando a translatePage desde changeLanguage');
    translatePage();
    
    // Actualizar el botón de idioma
    console.log('Llamando a updateLanguageToggle desde changeLanguage');
    updateLanguageToggle();
    
    console.log('changeLanguage completada');
}

// Function to translate the page
function translatePage() {
    const lang = window.currentLanguage || 'es';
    console.log('translatePage llamada con idioma:', lang);
    console.log('Elementos con data-translate:', document.querySelectorAll('[data-translate]').length);
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        console.log('Traduciendo elemento con clave:', key, 'elemento:', element.tagName);
        
        if (translations[lang] && translations[lang][key]) {
            // Check if it's an input placeholder
            if (element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
                console.log('Traducido placeholder:', key, 'a:', translations[lang][key]);
            } 
            // Check if it's an option value
            else if (element.tagName === 'OPTION' && element.value === '') {
                element.textContent = translations[lang][key];
                console.log('Traducido option:', key, 'a:', translations[lang][key]);
            }
            // Regular text content
            else {
                element.textContent = translations[lang][key];
                console.log('Traducido texto:', key, 'a:', translations[lang][key]);
            }
        } else {
            console.log('No se encontró traducción para:', key, 'en idioma:', lang);
            if (!translations[lang]) {
                console.error('No existe el idioma:', lang, 'en translations');
            }
        }
    });
    console.log('Página traducida al idioma:', lang);
}

// Function to update the language toggle button
function updateLanguageToggle() {
    console.log('updateLanguageToggle llamada');
    const languageToggle = document.getElementById('languageToggle');
    console.log('Botón de idioma encontrado:', languageToggle !== null);
    
    if (languageToggle) {
        const lang = window.currentLanguage || 'es';
        console.log('Idioma actual en updateLanguageToggle:', lang);
        
        // Si el idioma actual es español, mostramos EN (para cambiar a inglés)
        // Si el idioma actual es inglés, mostramos ES (para cambiar a español)
        const newText = lang === 'es' ? 'ES' : 'EN';
        languageToggle.textContent = newText;
        
        console.log('Botón de idioma actualizado de', languageToggle.textContent, 'a', newText);
        console.log('HTML del botón después de actualizar:', languageToggle.outerHTML);
    } else {
        console.error('No se encontró el botón de idioma con ID "languageToggle"');
    }
}

